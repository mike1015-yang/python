import math
# 自然對數
print(f"math.e: {math.e}")
print(f"math.pi: {math.pi}")
# 絕對值
print(f"math.fabs(-1.1): {math.fabs(-1.1)}")
print(f"max(1.1, 2.1): {max(1.1, 2.1)}")
print(f"min(1.1, 2.1): {min(1.1, 2.1)}")
# 四捨五入取整數
print(f"round(1.67): {round(1.67)}")
print(f"round(1.37): {round(1.37)}")
# ceil(x): 取大於x的最小整數
print(f"math.ceil(1.67): {math.ceil(1.67)}")
print(f"math.ceil(1.37): {math.ceil(1.37)}")
# floor(x): 取小於x的最大整數
print(f"math.floor(1.67): {math.floor(1.67)}")
print(f"math.floor(1.37): {math.floor(1.37)}")
print(f"math.pow(2, 3): {math.pow(2, 3)}")
# 開根號
print(f"math.sqrt(9): {math.sqrt(9)}")
# 角度轉弧度
print(f"math.radians(60): {math.radians(60)}")
# 弧度轉角度
print(f"math.degrees(1): {math.degrees(1)}")
# sin(x): x放的是弧度
print(f"math.sin(math.radians(60)): {math.sin(math.radians(60))}")

