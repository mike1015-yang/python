## 文字值
text1 = 'Hello, John.'
text2 = "Hi, Mary."
text3 = """
Hello, John.
Hi, Mary.
"""
print(text1)
print(type(text1))
print(text2)
print(text3)

## 跳脫字元
# 換行
s1 = "書名：\nPython入門"
print(s1)

# tab鍵，上下欄位對齊
s2 = "Python入門\t650元\nMySQL資料庫管理\t700元"
print(s2)

# 雙引號
s3 = "\"Python\"入門"
print(s3)

# 反斜線
s4 = "\\Python\\入門"
print(s4)