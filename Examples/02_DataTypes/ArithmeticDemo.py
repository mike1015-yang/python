i1 = 5
i2 = 2
d1 = 2.0

print(f"{i1} + {i2} = {i1 + i2}")
print(f"{i1} - {i2} = {i1 - i2}")
print(f"{i1} * {i2} * {d1} = {i1 * i2 * d1}")
print(f"{i1} / {i2} = {i1 / i2}")
# 整數除法 (商為整數，不為小數)
print(f"{i1} // {i2} = {i1 // i2}")
# 取餘數, modulo, mod
print(f"{i1} % {i2} = {i1 % i2}")
# 次方
print(f"{i1} ** {i2} = {i1 ** i2}")
